opcode_t opcode_new(uint8_t code, char *name, int len, int cycles, enum AddressingMode mode)
{
    opcode_t op;
    op.code = code;
    op.name = name;
    op.len = len;
    op.cycles = cycles;
    op.mode = mode;
    return op;
}

